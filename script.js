    function convertSpeed() {
      const inputValue = parseFloat(speedInput.value);
  
      if (isNaN(inputValue)) {
        convertedResult.textContent = "Please enter a valid number."; // Corrected message
        return;
      }
  
      const from = fromUnit.value;
      const to = toUnit.value;
  
      // Check if the units are valid
      if (!(from in conversions) || !(to in conversions)) {
        convertedResult.textContent = "Invalid units selected."; // Corrected message
        return;
      }
  
      // Perform the conversion
      const result = inputValue * conversions[from][to];
  
      // Display the result
      convertedResult.textContent = `Converted speed: ${result.toFixed(2)} ${to}`;
    }
  
